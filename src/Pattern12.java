public class Pattern12 {
	
      public static void main(String[] args) {
		
		  for(int i=1; i<=4; i++) {
			
			  for(int j=1; j<=i; j++) {
				  
				  
				  for(int k=4; k<=i; k++) {
					 
					  System.out.print(k+" ");
				  }
				
			  System.out.print(i+" "); 
			  }
			
			  System.out.println();
		}

	}
	
}
