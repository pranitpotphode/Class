package SWAP;

public class Swap2 {
	
public static void main(String[] args) {
		
		int num1 = 50, num2 = 100;
		
		int temp = num2;  //100
		
		num2 = num1;  //50
		
		num1 = temp; //100
		
		System.out.println("num1 is " + num1);  //100
		 
		System.out.println("num2 is " + num2);   //50
		
		

	}


}
