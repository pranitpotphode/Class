package IfElse;

public class ifElseif {
	
public static void main(String[] args) {
		
		int num = 100;
		
		if(num==10) {
			System.out.println("num is 10");
		}
		else if(num==20) {
			System.out.println("num is 20");
		}
		else if(num==75) {
			System.out.println("num is 75");
		}
		else if(num==1000) {
			System.out.println("num is 1000");
		}
		else {
			System.out.println("No condition is true");
		}

	}

}
