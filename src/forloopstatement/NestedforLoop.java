package forloopstatement;

public class NestedforLoop {
	
public static void main(String[] args) {
		

		for(int i=1; i<=2 ; i++) {
			
			System.out.println("Hello I am inside OUTER for loop");
			
			for(int j=1; j<=4 ; j++) {
				
				System.out.println("Hello I am inside INNER for loop");
				
			}
				
		}
		
	
	}
	

}
